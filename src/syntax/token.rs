#[derive(Debug, PartialEq, Clone)]
pub enum Token {
    Var,
    If,
    Else,
    New,
    Ident(String),
    String(String),
    Eof,
    Number(f64),
    Eq,
    EqEq,
    EqEqEq,
    FunctionKeyword,
    OpenParen,
    CloseParen,
    OpenCurly,
    CloseCurly,
    OpenSquare,
    CloseSquare,
    Comma,
    Dot,
    Plus,
    Minus,
    Bang,
    LogicalAnd,
}
